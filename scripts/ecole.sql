CREATE TABLE COMPETENCE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOM VARCHAR2(100) NOT NULL,
    DESCRIPTION VARCHAR2(255),
    CONSTRAINT PK_COMPETENCE PRIMARY KEY (ID)
);

CREATE TABLE INTERPRETE (
    LOGIN VARCHAR2(50) NOT NULL,
    NOM VARCHAR2(100) NOT NULL,
    PRENOM VARCHAR2(100) NOT NULL,
    CONSTRAINT PK_INTERPRETE PRIMARY KEY (LOGIN)
);

CREATE TABLE INDISPONIBILITE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    DATE_INDISPONIBILITE DATE NOT NULL,
    HEURE_DEBUT DATE NOT NULL,
    HEURE_FIN DATE NOT NULL,
    LOGIN_INTERPRETE VARCHAR2(50) NOT NULL,
    CONSTRAINT PK_INDISPONIBILITE PRIMARY KEY (ID),
    CONSTRAINT FK_INDISPO_INTERPRETE
        FOREIGN KEY (LOGIN_INTERPRETE)
        REFERENCES INTERPRETE(LOGIN)
);

CREATE TABLE INTERPRETE_COMPETENCE (
    LOGIN_INTERPRETE VARCHAR2(50) NOT NULL,
    ID_COMPETENCE NUMBER NOT NULL,
    CONSTRAINT PK_INTERPRETE_COMPETENCE
        PRIMARY KEY (LOGIN_INTERPRETE, ID_COMPETENCE),
    CONSTRAINT FK_IC_INTERPRETE
        FOREIGN KEY (LOGIN_INTERPRETE)
        REFERENCES INTERPRETE(LOGIN),
    CONSTRAINT FK_IC_COMPETENCE
        FOREIGN KEY (ID_COMPETENCE)
        REFERENCES COMPETENCE(ID)
);

CREATE TABLE INTERPRETE_INDISPONIBILITE (
    LOGIN_INTERPRETE VARCHAR2(50) NOT NULL,
    ID_INDISPONIBILITE NUMBER NOT NULL,
    CONSTRAINT PK_INTERPRETE_INDISPONIBILITE
        PRIMARY KEY (LOGIN_INTERPRETE, ID_INDISPONIBILITE),
    CONSTRAINT FK_II_INTERPRETE
        FOREIGN KEY (LOGIN_INTERPRETE)
        REFERENCES INTERPRETE(LOGIN),
    CONSTRAINT FK_II_INDISPONIBILITE
        FOREIGN KEY (ID_INDISPONIBILITE)
        REFERENCES INDISPONIBILITE(ID)
);